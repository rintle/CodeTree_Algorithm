# 대충 DP숫자를 1 올릴 때, 10번 반복하기 때문에 
# 약 1000x10 = 1만번 반복 루프만 돌면 해결 가능
# 2차원 배열로 유지하는것이 정신건강에 옳다.

DP = [[0 for _ in range(10)] for _ in range(1001)]
for i in range(10):
    DP[1][i] = 1
DP_1 = sum(DP[1])
for i in range(10):
    DP[2][i] += DP_1
    DP_1 -= DP[1][i]

N = int(input())
for i in range(3, N+1):
    tmp = sum(DP[i-1])
    for j in range(10):
        DP[i][j] = tmp
        tmp -= DP[i-1][j]
        
for d in DP:
    print(*d)
print(sum(DP[N])%10007)